#include<stdio.h>
#include<stdlib.h>

int main(){
	int j1=0, j2=0,r1=0, rodadas=0,i=0,partida=0;
	while(1==1){
		j1=0;j2=0;rodadas=0;i=0;r1=0;
		scanf("%d",&rodadas);
		if(rodadas==0)break;
		partida++;
		printf("Teste %d\n",partida);
		for(i;i<rodadas;i++){
			scanf("%d %d",&j1, &j2);
			r1+=j1-j2;
			printf("%d\n",r1);
		}
		printf("\n");
	}
	return 0;
}
